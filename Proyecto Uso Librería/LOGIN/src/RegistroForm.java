         String nombre = txtNombre.getText();
         String ApellidoP = txtApellidoP.getText();
         String ApellidoM = txtApellidoM.getText();
    String correo = txtCorreo.getText();
    String contraseña = new String(txtContraseña.getPassword());

    if (!ValidadorRegistro.validarNombre(nombre)) {
        lblError.setText("El nombre solo puede contener letras.");
        return;
    }

    if (!ValidadorRegistro.validarCorreo(correo)) {
        lblError.setText("El correo debe ser válido y sin números.");
        return;
    }
    if (!ValidadorRegistro.validarApellidoP(ApellidoP)) {
        lblError.setText("El Apellido Paterno solo puede contener letras.");
        return;
    }
    
    if (!ValidadorRegistro.validarApellidoM(ApellidoM)) {
        lblError.setText("El Apellido Materno solo puede contener letras.");
        return;
    }


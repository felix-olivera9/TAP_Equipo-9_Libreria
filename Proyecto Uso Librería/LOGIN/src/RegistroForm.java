    private void alternarVisibilidadContraseña() {
    if (mostrar) {
        // Ocultar contraseña
        txtContraseña.setEchoChar((char) 8226);  
        txtConfirmarContraseña.setEchoChar((char) 8226);
        lblMostrarContraseña.setIcon(new ImageIcon(getClass().getResource("/iconos/icons8-eye-24.png"))); 
        
    } else {
        // Mostrar contraseña
        txtContraseña.setEchoChar((char) 0); 
        txtConfirmarContraseña.setEchoChar((char) 0); 
        lblMostrarContraseña.setIcon(new ImageIcon(getClass().getResource("/iconos/icons8-eye-24.png")));
        
    }
    mostrar = !mostrar; // Alternar estado
}
    
